package hk220915;

public class Variable2 {
	public static void main (String [] args) {
		
		int level; // 변수 선언 : 선언 할 때만 자료형을 사용 
		level = 10; // 변수에 값 대입
		System.out.println(level);
		// int : 정수 : 소수점을 가지지 않은 수 : 1, 20, 100
		level = 100;
		System.out.println(level);
		//level = 10.5; level을 int(정수)로 선언해서 실수는 안됨
		// 실수 : 소수점을 가진 수
		
		int levell = 1000; //다른 이름의 변수선언은 가능
		System.out.println(levell);
		
	}
}
